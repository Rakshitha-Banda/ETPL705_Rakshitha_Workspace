package com.evergent.corejava.rakshitha.application4;
//category is a abstract class and it is implemented product interface
abstract class Category implements Product {
     String name;
     int price;

    public Category(String name, int price) {
    	//this keyword is refers to instance variables
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }
}
